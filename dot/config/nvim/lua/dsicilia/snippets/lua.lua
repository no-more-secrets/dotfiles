-----------------------------------------------------------------
-- Lua snippets.
-----------------------------------------------------------------
local assembler = require( 'dsicilia.snippets.assemble' )
local S = {}

-----------------------------------------------------------------
-- Snippets.
-----------------------------------------------------------------
S.section =
[[-----------------------------------------------------------------
-- $1
-----------------------------------------------------------------
$0]]

S.lfunc =
[[local function $1( $2 )
  $0
end

]]

S.mfunc =
[[function M.$1( $2 )
  $0
end

]]

S.module =
[[-----------------------------------------------------------------
-- $1
-----------------------------------------------------------------
local M = {}

-----------------------------------------------------------------
-- Imports.
-----------------------------------------------------------------
$2

-----------------------------------------------------------------
-- Aliases.
-----------------------------------------------------------------
$3

-----------------------------------------------------------------
-- Implementation.
-----------------------------------------------------------------
$0

-----------------------------------------------------------------
-- Finished.
-----------------------------------------------------------------
return M]]

-----------------------------------------------------------------
-- Assemble and parse snippets.
-----------------------------------------------------------------
return assembler.assemble( S )
